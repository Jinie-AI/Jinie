class TestGetPayload:

    def test_returns_code_payload_type(self, loaded_ce):
        assert isinstance(loaded_ce.get_payload(), CodePayload)

    def test_language_is_dart_for_dart_file(self, loaded_ce):
        assert loaded_ce.get_payload().language == "dart"

    def test_language_is_python_for_py_file(self, fh, tmp_path):
        path = str(tmp_path / "utils.py")
        Path(path).write_text("def greet(): pass\n", encoding="utf-8")
        cp = CodeEditor().load_file(fh.read(path)).get_payload()
        assert cp.language == "python"

    def test_language_is_yaml_for_pubspec(self, fh, tmp_path):
        path = str(tmp_path / "pubspec.yaml")
        Path(path).write_text("name: jinie_store\n", encoding="utf-8")
        cp = CodeEditor().load_file(fh.read(path)).get_payload()
        assert cp.language == "yaml"

    def test_edited_content_reflects_all_replacements(self, loaded_ce):
        loaded_ce.replace_token("__WIDGET_NAME__",  "ProductCard")
        loaded_ce.replace_token("__PRIMARY_COLOR__","Color(0xFF1A73E8)")
        loaded_ce.replace_token("__CURRENCY__",     "PKR")
        cp = loaded_ce.get_payload()
        assert "ProductCard"        in cp.edited_content
        assert "Color(0xFF1A73E8)"  in cp.edited_content
        assert "PKR"                in cp.edited_content
        assert "__WIDGET_NAME__"    not in cp.edited_content

    def test_original_content_always_unchanged(self, fh, dart_file):
        path, original = dart_file
        ce = CodeEditor()
        ce.load_file(fh.read(path))
        ce.replace_token("__WIDGET_NAME__", "ProductCard")
        cp = ce.get_payload()
        assert "__WIDGET_NAME__" in cp.original_content   # original kept
        assert "ProductCard"     in cp.edited_content     # edit applied

    def test_trace_id_prefixed_CE(self, loaded_ce):
        assert loaded_ce.get_payload().trace_id.startswith("CE-")

    def test_source_trace_id_links_back_to_file_payload(self, fh, dart_file):
        path, _ = dart_file
        fp = fh.read(path)
        cp = CodeEditor().load_file(fp).get_payload()
        assert cp.source_trace_id == fp.trace_id

    def test_edit_history_captures_ops_in_order(self, loaded_ce):
        loaded_ce.insert_lines(0, ["// header"])
        loaded_ce.replace_token("__WIDGET_NAME__", "ProductCard")
        loaded_ce.delete_lines(1, 1)
        ops = [r.operation for r in loaded_ce.get_payload().edit_history]
        assert ops == ["INSERT", "REPLACE_TOKEN", "DELETE"]

    def test_get_payload_is_idempotent(self, loaded_ce):
        p1 = loaded_ce.get_payload()
        p2 = loaded_ce.get_payload()
        assert p1.edited_content == p2.edited_content
        assert p1.trace_id       == p2.trace_id

    def test_before_load_raises_no_file_loaded(self, ce):
        with pytest.raises(CodeEditorError) as exc:
            ce.get_payload()
        assert exc.value.code == "NO_FILE_LOADED"

    def test_source_path_is_absolute(self, loaded_ce):
        assert os.path.isabs(loaded_ce.get_payload().source_path)

    def test_extension_is_dart(self, loaded_ce):
        assert loaded_ce.get_payload().extension == ".dart"


