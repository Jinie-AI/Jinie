class TestMdConverterContract:

    def test_edited_content_is_non_empty_string(self, loaded_ce):
        """MdConverter needs a non-empty string to wrap in a code block."""
        cp = loaded_ce.get_payload()
        assert isinstance(cp.edited_content, str)
        assert len(cp.edited_content) > 0

    def test_language_field_is_dart(self, loaded_ce):
        """MdConverter uses language for ```dart fenced block tag."""
        assert loaded_ce.get_payload().language == "dart"

    def test_source_path_provided_for_attribution(self, loaded_ce):
        """MdConverter adds source_path as attribution in the Markdown."""
        cp = loaded_ce.get_payload()
        assert cp.source_path
        assert os.path.isabs(cp.source_path)

    def test_trace_id_for_traceability_matrix(self, loaded_ce):
        """
        MdConverter chains this CE trace_id into Module 03
        (Traceability Manager) so every Markdown block is traceable.
        """
        assert loaded_ce.get_payload().trace_id.startswith("CE-")

    def test_fluent_chain_and_step_by_step_produce_same_output(
            self, fh, tmp_path):
        """
        Fluent API and step-by-step must give identical CodePayloads.
        MdConverter must receive the same content either way.
        """
        content = "class __WIDGET_NAME__ {}\n"
        path    = str(tmp_path / "w.dart")
        Path(path).write_text(content, encoding="utf-8")

        # fluent
        cp_fluent = (
            CodeEditor()
            .load_file(fh.read(path))
            .replace_token("__WIDGET_NAME__", "ProductCard")
            .get_payload()
        )

        # step-by-step
        ce = CodeEditor()
        ce.load_file(fh.read(path))
        ce.replace_token("__WIDGET_NAME__", "ProductCard")
        cp_step = ce.get_payload()

        assert cp_fluent.edited_content == cp_step.edited_content
        assert cp_fluent.language       == cp_step.language
        assert cp_fluent.extension      == cp_step.extension