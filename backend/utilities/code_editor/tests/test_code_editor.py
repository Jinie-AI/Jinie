class TestInsertLines:

    def test_prepend_at_zero_inserts_before_line_1(self, loaded_ce):
        """Compiler adds Jinie header at very top of Dart file."""
        first_before = loaded_ce.preview(1, 1)
        loaded_ce.insert_lines(0, ["// Generated by Jinie — do not edit"])
        assert loaded_ce.preview(1, 1) == "// Generated by Jinie — do not edit"
        assert loaded_ce.preview(2, 2) == first_before

    def test_insert_at_end_appends(self, loaded_ce):
        old_count = loaded_ce.line_count
        loaded_ce.insert_lines(old_count, ["// end of file"])
        assert loaded_ce.line_count == old_count + 1
        assert loaded_ce.preview(loaded_ce.line_count) == "// end of file"

    def test_insert_multiple_import_lines(self, loaded_ce):
        """Compiler inserts several import lines at top."""
        old_count = loaded_ce.line_count
        imports = [
            "import 'package:provider/provider.dart';",
            "import 'package:jinie_store/models/product.dart';",
        ]
        loaded_ce.insert_lines(0, imports)
        assert loaded_ce.line_count == old_count + 2
        assert loaded_ce.preview(1, 1) == imports[0]
        assert loaded_ce.preview(2, 2) == imports[1]

    def test_out_of_range_raises_error(self, loaded_ce):
        with pytest.raises(CodeEditorError) as exc:
            loaded_ce.insert_lines(9999, ["oops"])
        assert exc.value.code == "LINE_OUT_OF_RANGE"

    def test_without_load_raises_no_file_loaded(self, ce):
        with pytest.raises(CodeEditorError) as exc:
            ce.insert_lines(0, ["x"])
        assert exc.value.code == "NO_FILE_LOADED"

    def test_returns_self_for_chaining(self, loaded_ce):
        assert loaded_ce.insert_lines(0, ["// h"]) is loaded_ce

    def test_last_receipt_operation_is_insert(self, loaded_ce):
        loaded_ce.insert_lines(0, ["// h"])
        assert loaded_ce.last_receipt.operation == "INSERT"

    def test_last_receipt_snapshot_line_count(self, loaded_ce):
        old_count = loaded_ce.line_count
        loaded_ce.insert_lines(0, ["// h"])
        assert loaded_ce.last_receipt.snapshot_line_count == old_count + 1


