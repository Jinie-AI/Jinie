class TestLoadFile:

    def test_raises_type_error_for_raw_string(self, ce):
        with pytest.raises(TypeError):
            ce.load_file("class ProductCard {}")

    def test_raises_type_error_for_none(self, ce):
        with pytest.raises(TypeError):
            ce.load_file(None)  # type: ignore

    def test_accepts_file_payload(self, fh, ce, dart_file):
        path, _ = dart_file
        ce.load_file(fh.read(path))   # must not raise
        assert ce.line_count > 0

    def test_returns_self_for_fluent_api(self, fh, ce, dart_file):
        path, _ = dart_file
        result = ce.load_file(fh.read(path))
        assert result is ce

    def test_sets_line_count_correctly(self, fh, ce, dart_file):
        path, content = dart_file
        ce.load_file(fh.read(path))
        assert ce.line_count == len(content.splitlines())

    def test_reload_clears_edit_history(self, fh, dart_file):
        path, _ = dart_file
        ce = CodeEditor()
        ce.load_file(fh.read(path))
        ce.insert_lines(0, ["// header"])
        ce.load_file(fh.read(path))        # reload
        assert ce.get_payload().edit_history == []

    def test_last_receipt_is_none_after_load(self, fh, ce, dart_file):
        path, _ = dart_file
        ce.load_file(fh.read(path))
        assert ce.last_receipt is None


