class TestReplaceToken:

    def test_replace_widget_name_placeholder(self, loaded_ce):
        """
        __WIDGET_NAME__ appears twice (class def + constructor).
        Both must be replaced.
        """
        loaded_ce.replace_token("__WIDGET_NAME__", "ProductCard")
        text = loaded_ce.preview()
        assert "ProductCard"      in text
        assert "__WIDGET_NAME__"  not in text
        assert loaded_ce.last_receipt.replacements == 2

    def test_replace_primary_color_placeholder(self, loaded_ce):
        loaded_ce.replace_token("__PRIMARY_COLOR__", "Color(0xFF1A73E8)")
        assert "Color(0xFF1A73E8)"  in loaded_ce.preview()
        assert "__PRIMARY_COLOR__"  not in loaded_ce.preview()

    def test_replace_currency_placeholder(self, loaded_ce):
        loaded_ce.replace_token("__CURRENCY__", "PKR")
        assert "PKR"          in loaded_ce.preview()
        assert "__CURRENCY__" not in loaded_ce.preview()

    def test_replace_all_placeholders_in_chain(self, fh, tmp_path):
        """Full Compiler chain — all three placeholders replaced."""
        content = (
            "class __WIDGET_NAME__ {\n"
            "  Color c = __PRIMARY_COLOR__;\n"
            "  String cur = '__CURRENCY__';\n"
            "}\n"
        )
        path = str(tmp_path / "w.dart")
        Path(path).write_text(content, encoding="utf-8")

        text = (
            CodeEditor()
            .load_file(fh.read(path))
            .replace_token("__WIDGET_NAME__",  "CartItem")
            .replace_token("__PRIMARY_COLOR__","Color(0xFF000000)")
            .replace_token("__CURRENCY__",     "PKR")
            .get_payload()
            .edited_content
        )

        assert "CartItem"           in text
        assert "Color(0xFF000000)"  in text
        assert "PKR"                in text
        assert "__WIDGET_NAME__"    not in text
        assert "__PRIMARY_COLOR__"  not in text
        assert "__CURRENCY__"       not in text

    def test_replace_specific_occurrence_only(self, fh, tmp_path):
        content = "foo foo foo"
        path = str(tmp_path / "t.txt")
        Path(path).write_text(content, encoding="utf-8")
        ce = CodeEditor()
        ce.load_file(fh.read(path))
        ce.replace_token("foo", "bar", occurrence=2)
        assert ce.preview() == "foo bar foo"

    def test_token_not_found_raises(self, loaded_ce):
        with pytest.raises(CodeEditorError) as exc:
            loaded_ce.replace_token("__DOES_NOT_EXIST__", "x")
        assert exc.value.code == "TOKEN_NOT_FOUND"

    def test_nth_occurrence_out_of_range_raises(self, fh, tmp_path):
        path = str(tmp_path / "t.txt")
        Path(path).write_text("a a", encoding="utf-8")
        ce = CodeEditor()
        ce.load_file(fh.read(path))
        with pytest.raises(CodeEditorError) as exc:
            ce.replace_token("a", "b", occurrence=99)
        assert exc.value.code == "TOKEN_NOT_FOUND"

    def test_without_load_raises_no_file_loaded(self, ce):
        with pytest.raises(CodeEditorError) as exc:
            ce.replace_token("x", "y")
        assert exc.value.code == "NO_FILE_LOADED"

    def test_returns_self_for_chaining(self, loaded_ce):
        assert loaded_ce.replace_token("__WIDGET_NAME__", "X") is loaded_ce


